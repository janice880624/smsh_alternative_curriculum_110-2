function MainClick (){
  console.log("web 被點擊")
  swal({
    title: "小組網站 Demo",
    text: "請選擇瀏覽模式",
    icon: "success",
    buttons: {
      Btn: false,
      first: {
        text: "Web",
        visible: true
      },
      second: {
        text: "Code",
        visible: true
      }
    }
  }).then((value) => {
    if (value === "first") {
      window.location.href =
        "https://smsh-newtech.github.io/maincode/";
    }
    if (value === "second") {
      window.location.href =
        "https://github.com/smsh-newtech/maincode";
    }
  });
}

